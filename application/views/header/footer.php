<script>
	function showSuccessSweetDialog(successMessage) {
		Swal.fire({
			title: successMessage,
			icon: 'success',
			customClass: {
				confirmButton: 'btn btn-primary'
			},
			buttonsStyling: false
		});
	}
	function showLoadingDialog() {
		"use strict"
		Swal.fire({
			title: "Saving...",
			imageUrl: "<?php echo base_url();?>assets/images/ajax-loader.gif",
			imageWidth: 100,
			imageHeight: 100,
			showConfirmButton: false

		});
	}
	$(function() {
		"use strict"
		$("#newuser").submit(function (e) {
			e.preventDefault();

			var newuser = new FormData(this);
			showLoadingDialog();

			$.ajax({
				url: 'newuser',
				type: 'post',
				data: newuser.serialize(),
				success: function(response){
					console.log(response);
					if(response.status == 'success') {
						// $("#newuser").hide();
						Swal.close();
						var successMessage = "user created successfully";
						showSuccessSweetDialog(successMessage);
						console.log(successMessage);
					}
					else {
						$("#message").html(response.message);
					}

				}
			});
		});
	});



</script>
